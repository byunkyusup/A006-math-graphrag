def main():
    random.seed(SEED)
    os.makedirs(DATA_DIR, exist_ok=True)

    nodes = {}
    links = []
    # 노드 생성 + 영역 내 계열 연결
    area_chain = {}  # area -> 마지막 노드 id (등장 순서로 체인)
    for grade, areas in CURRICULUM.items():
        depth = list(CURRICULUM).index(grade)
        for area, concepts in areas:
            for concept in concepts:
                nid = f"{grade}:{concept}"
                rate = max(35, min(95, random.gauss(82 - depth * 3.0, 8)))
                nodes[nid] = {
                    "id": nid,
                    "label": concept,
                    "grade": grade,
                    "area": area,
                    "items": random.randint(8, 30),
                    "correctRate": round(rate, 1),
                    "keywords": [w for w in concept.replace("의", " ").replace("와", " ").replace("과", " ").split() if len(w) >= 2],
                }
                prev = area_chain.get(area)
                if prev:
                    links.append({"source": prev, "target": nid})
                area_chain[area] = nid

    # 영역 간 선수관계 보강 (양쪽 노드 존재 시에만)
    existing = {(l["source"], l["target"]) for l in links}
    for s, t in CROSS_PREREQ:
        if s in nodes and t in nodes and (s, t) not in existing:
            links.append({"source": s, "target": t})
            existing.add((s, t))

    # 노드 크기(val) = 연결 차수 기반
    deg = {nid: 0 for nid in nodes}
    for l in links:
        deg[l["source"]] += 1
        deg[l["target"]] += 1
    for nid, n in nodes.items():
        n["val"] = 3 + deg[nid] * 2

    out = {"nodes": list(nodes.values()), "links": links}
    with open(os.path.join(DATA_DIR, "graph.json"), "w", encoding="utf-8") as f:
        json.dump(out, f, ensure_ascii=False, indent=2)

    print(f"노드(개념): {len(nodes)}개")
    print(f"엣지(선수관계): {len(links)}개")
    print(f"영역: {sorted({n['area'] for n in nodes.values()})}")
    print(f"저장: {os.path.join(DATA_DIR, 'graph.json')}")


if __name__ == "__main__":
    main()
